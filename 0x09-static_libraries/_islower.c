/**
 * _islower - tests  whether the passed character is a lowercase letter
 * @c: the character to be to be tested
 *
 * Return: 1  if c is a lowercase,and return zero if c is not
*/
int _islower(int c)

{

	if (c >= 'a' && c <= 'z')

	return (1);

	else


	return (0);

}

